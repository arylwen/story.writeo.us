package com.halcyon.storywriter;

public class StructureFileTemplate {
	private String name;
	private String file;

	public void setName(String aName)
	{
		name = aName;
	}

	public String getName(){
		return name;
	}

	public void setFile(String aFile){
		file = aFile;
	}

	public String getFile()
	{
		return file;
	}
}
