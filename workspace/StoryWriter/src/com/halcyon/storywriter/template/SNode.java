package com.halcyon.storywriter.template;

public class SNode
{
	private String name;
	private int color;
	private int id;

	public void setName(String aName)
	{
		name = aName;
	}

	public String getName()
	{
		return name;
	}

	public void setColor(int aColor)
	{
		color = aColor;
	}

	public int getColor()
	{
		return color;
	}
	
	public void setId(int aId)
	{
		id = aId;
	}

	public int getId()
	{
		return id;
	}
}
